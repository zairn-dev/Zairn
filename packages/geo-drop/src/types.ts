// =====================
// Persistence (DB-independent recovery)
// =====================

/**
 * Persistence level
 * - db-only: DB storage only (default, free)
 * - ipfs: Metadata also pinned to IPFS (recoverable even if DB is lost, requires ongoing pinning)
 * - onchain: Metadata CID recorded on-chain (implicitly pins to IPFS as well)
 * - ipfs+onchain: Explicitly both
 */
export type PersistenceLevel = 'db-only' | 'ipfs' | 'onchain' | 'ipfs+onchain';

/**
 * Abstract interface for an EVM signer
 * Can be adapted for either ethers.js Signer or viem WalletClient
 */
export interface EvmSigner {
  getAddress(): Promise<string>;
  sendTransaction(tx: {
    to: string;
    data: string;
    value?: string;
  }): Promise<{ hash: string; wait(confirmations?: number): Promise<{ status: number }> }>;
}

/**
 * Chain configuration
 */
export interface ChainConfig {
  /** EVM JSON-RPC endpoint */
  rpcUrl: string;
  /** Deployed GeoDropRegistry contract address */
  registryAddress: string;
  /** Signer (required for write operations; optional for read-only) */
  signer?: EvmSigner;
  /** Chain ID (optional, for logging) */
  chainId?: number;
  /** Number of confirmations to wait (default: 2 for L1, 1 for L2) */
  confirmations?: number;
}

/**
 * Persistence configuration
 */
export interface PersistenceConfig {
  level: PersistenceLevel;
  chain?: ChainConfig;
  /** Whether to fail drop creation if persistence fails (default: false) */
  strict?: boolean;
}

/**
 * Metadata document stored on IPFS
 * This document alone is sufficient to decrypt content even if DB is lost
 */
export interface DropMetadataDocumentV1 {
  version: 1;
  dropId: string;
  geohash: string;
  contentCid: string;
  encryptionSalt: string;
  unlockRadiusMeters: number;
  contentType: DropContentType;
  title: string;
  proofConfig: ProofConfig | null;
  createdAt: string;
  encrypted?: boolean;
}

export interface DropMetadataDocumentV2 {
  version: 2;
  dropId: string;
  geohash: string;
  contentCid: string;
  encryptionSalt: string;
  unlockRadiusMeters: number;
  contentType: DropContentType;
  title: string;
  proofConfig: ProofConfig | null;
  createdAt: string;
  encrypted?: boolean;
  /** Key derivation version (1 or 2). Required for decryption. */
  keyDerivationVersion: 1 | 2;
  /** Encryption algorithm identifier for future migration */
  encryptionAlgorithm: 'aes-256-gcm';
  /** PBKDF2 iteration count (for key stretching migration) */
  pbkdf2Iterations: number;
  /** Pinning providers used (for monitoring/renewal) */
  pinningProviders?: string[];
  /** Content size in bytes (for storage accounting) */
  contentSizeBytes?: number;
  /** Server secret version used to encrypt (for recovery decryption) */
  serverSecretVersion?: number;
}

/** Union type for all metadata versions */
export type DropMetadataDocument = DropMetadataDocumentV1 | DropMetadataDocumentV2;

