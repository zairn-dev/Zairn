export interface GeoDropOptions {
  supabaseUrl: string;
  supabaseAnonKey: string;
  ipfs?: IpfsConfig;
  /** Initial custom proof verifiers to register */
  verifiers?: Record<string, ProofVerifier>;
  /**
   * Image proof Edge Function URL
   * Defaults to supabaseUrl + '/functions/v1/image-proof' when unspecified
   */
  imageProofUrl?: string;
  /**
   * Enable server-side unlock via Edge Function (recommended for production).
   * When true, unlockDrop calls the unlock-drop Edge Function instead of
   * decrypting client-side, so encryption keys never reach the client.
   * Defaults to supabaseUrl + '/functions/v1/unlock-drop'
   */
  serverUnlock?: boolean | string;
  /**
   * IPFS proxy Edge Function URL.
   * When set, IPFS operations go through the proxy and API keys stay server-side.
   * Defaults to supabaseUrl + '/functions/v1/ipfs-proxy' when serverUnlock is enabled.
   */
  ipfsProxyUrl?: string;
  /** Persistence configuration */
  persistence?: PersistenceConfig;
  /**
   * Server-side encryption secret(s) for location key derivation.
   *
   * Option A: Single secret (backward compatible)
   *   encryptionSecret: 'my-secret'
   *
   * Option B: Versioned secret map (enables rotation)
   *   encryptionSecrets: { 1: 'old-secret', 2: 'new-secret' }
   *   currentSecretVersion: 2
   *
   * When serverUnlock is disabled, at least one secret is REQUIRED
   * for production — without it, encryption keys are predictable
   * from publicly-visible DB columns.
   *
   * Set allowInsecureNoSecret: true to suppress the error (dev only).
   */
  encryptionSecret?: string;
  /** Versioned secret map for key rotation. Keys are version numbers. */
  encryptionSecrets?: Record<number, string>;
  /** Which secret version to use for NEW drops (default: 1 or max key in encryptionSecrets) */
  currentSecretVersion?: number;
  /** Allow SDK to run without encryption secret (dev/testing only) */
  allowInsecureNoSecret?: boolean;
  /**
   * SBPP: encrypted search configuration for Search-Bound Proximity Proofs.
   * When set, enables findNearbyDropsSbpp() and initSearchSession().
   */
  encryptedSearchConfig?: { searchKey: string; precisionLevels?: number[] };
}

export interface GeoDropSDK {
  // Drop CRUD
  createDrop: (data: GeoDropCreate, content: File | Blob | string) => Promise<GeoDrop>;
  getDrop: (dropId: string) => Promise<GeoDrop | null>;
  getMyDrops: (options?: { status?: DropStatus; limit?: number }) => Promise<GeoDrop[]>;
  deleteDrop: (dropId: string) => Promise<void>;

  // Discovery & unlock
  findNearbyDrops: (lat: number, lon: number, radiusMeters?: number) => Promise<NearbyDrop[]>;
  unlockDrop: (dropId: string, lat: number, lon: number, accuracy: number, password?: string, proofs?: ProofSubmission[]) => Promise<UnlockResult>;

  // Location proof
  getProofConfig: (dropId: string) => Promise<ProofConfig | null>;
  registerVerifier: (methodOrId: ProofMethodType | string, verifier: ProofVerifier) => void;

  // Image proof (server-side DINOv2)
  /** Extract feature vector from a reference image (used during drop creation) */
  extractImageEmbedding: (imageBase64: string) => Promise<{ embedding: number[]; dimensions: number }>;
  /** Compare a captured image against a drop's reference and verify (for debug/direct calls) */
  verifyImageProof: (imageBase64: string, dropId: string, threshold?: number) => Promise<{ verified: boolean; similarity: number }>;

  // Private sharing
  shareDrop: (dropId: string, userIds: string[]) => Promise<void>;
  unshareDrop: (dropId: string, userId: string) => Promise<void>;
  getSharedDrops: () => Promise<GeoDrop[]>;

  // Claims
  getDropClaims: (dropId: string) => Promise<DropClaim[]>;
  getMyClaims: (options?: { limit?: number }) => Promise<DropClaim[]>;

  // Stats
  getMyStats: () => Promise<DropStats>;

  // IPFS
  uploadToIpfs: (content: File | Blob | string) => Promise<IpfsUploadResult>;
  fetchFromIpfs: (cid: string) => Promise<string>;

  // Geofence verification
  verifyLocation: (dropId: string, lat: number, lon: number, accuracy: number) => Promise<LocationProof>;

  // Realtime
  subscribeNearbyDrops: (lat: number, lon: number, radiusMeters: number, onDrop: (drop: GeoDrop) => void) => import('@supabase/supabase-js').RealtimeChannel;

  // NFT metadata generation
  generateNftMetadata: (drop: GeoDrop, imageUrl?: string) => NftMetadata;

  // DB-independent recovery
  /** Recover directly from a metadata CID */
  recoverDrop: (metadataCid: string, recoverySecret?: string) => Promise<RecoveredDrop>;
  /** Search the on-chain index by location and recover */
  discoverDropsByLocation: (lat: number, lon: number, precision?: number) => Promise<RecoveredDrop[]>;
  /** Decrypt recovered drop content */
  decryptRecoveredDrop: (recovered: RecoveredDrop) => Promise<string>;

  // Key rotation
  /** Re-encrypt a drop from old secret version to new secret version */
  reEncryptDrop: (dropId: string, oldSecretVer: number, newSecretVer: number) => Promise<void>;
  /** Export encryption secrets as a password-encrypted backup blob */
  exportEncryptedSecrets: (masterPassword: string) => Promise<string>;
  /** Import encryption secrets from a backup blob */
  importEncryptedSecrets: (blob: string, masterPassword: string) => Promise<Record<number, string>>;

  // SBPP (Search-Bound Proximity Proofs)
  /** Initialize an SBPP search session. Returns a session with nonce for binding. */
  initSearchSession?: (ttlMs?: number) => { sessionId: string; nonce: string; createdAt: number; expiresAt: number };
  /** Find nearby drops using encrypted search tokens, bound to an SBPP session. */
  findNearbyDropsSbpp?: (lat: number, lon: number, radiusMeters: number, session: { sessionId: string; nonce: string; createdAt: number; expiresAt: number }) => Promise<NearbyDrop[]>;

  // Utilities
  encodeGeohash: (lat: number, lon: number, precision?: number) => string;
  decodeGeohash: (geohash: string) => { lat: number; lon: number };
  calculateDistance: (lat1: number, lon1: number, lat2: number, lon2: number) => number;
}
