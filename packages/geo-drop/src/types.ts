// =====================
// Location proof methods
// =====================

/**
 * Proof method type
 * - gps: GPS coordinates (default)
 * - secret: ID/secret matching (front-end sends an ID obtained via QR, BLE, WiFi, NFC, etc.)
 * - ar: Image feature-based location verification
 * - zkp: Zero-knowledge proof of proximity (Groth16) — proves location without revealing coordinates
 * - zkp-region: Zero-knowledge proof of polygon containment (Groth16) — proves inside a region
 * - custom: Fully custom verifier
 */
export type ProofMethodType = 'gps' | 'secret' | 'ar' | 'zkp' | 'zkp-region' | 'custom';

/**
 * Proof requirements set on a drop
 * When multiple are specified, mode (all/any) switches between AND/OR
 */
export interface ProofRequirement {
  /** Proof method type */
  method: ProofMethodType;
  /**
   * Method-specific parameters
   * - gps: {} (default; controlled by unlock_radius_meters)
   * - secret: { secret: string; label?: string } — secret obtained on-site (via QR/BLE/WiFi/NFC etc., acquisition method is up to the front-end)
   * - ar: { reference_embedding?: number[]; reference_embeddings?: number[][]; similarity_threshold?: number; max_age_seconds?: number } — DINOv2 feature vector(s). Multiple embeddings for multi-angle matching (client picks max similarity). max_age_seconds rejects stale captures
   * - zkp: { verification_key: object; artifacts_url?: string; policy_version?: string; epoch?: string|number; server_nonce?: string; drop_id?: string } — Groth16 verification key for Zairn-ZKP. Optional context fields bind proofs to a drop/session statement
   * - zkp-region: { verification_key: object; polygon: {lat: number; lon: number}[]; artifacts_url?: string; policy_version?: string; epoch?: string|number; server_nonce?: string; drop_id?: string } — polygon containment proof
   * - custom: { verifier_id: string; [key: string]: unknown } — parameters passed to a custom verifier
   */
  params: Record<string, unknown>;
  /** Whether this method is required or optional (default: true) */
  required?: boolean;
}

/**
 * Proof configuration for a drop
 */
export interface ProofConfig {
  /**
   * Verification mode
   * - 'all': All required proofs must be satisfied (AND)
   * - 'any': Any single proof is sufficient (OR)
   * Default: 'all'
   */
  mode: 'all' | 'any';
  /** List of proof requirements */
  requirements: ProofRequirement[];
}

/**
 * Proof response submitted by a user
 */
export interface ProofSubmission {
  /** Proof method type */
  method: ProofMethodType;
  /**
   * Method-specific response data
   * - gps: { lat: number; lon: number; accuracy: number }
   * - secret: { secret: string } — secret obtained on-site (acquisition method does not matter)
   * - ar: { image: string; captured_at?: string; image_width?: number; image_height?: number } — base64 of captured image. captured_at (ISO 8601) for freshness check. Dimensions for screenshot detection
   * - zkp: { proof: Groth16Proof; publicSignals: string[]; statement?: ZkStatementBinding } — ZK proximity proof generated by snarkjs, optionally including the resolved public statement binding
   * - zkp-region: { proof: Groth16Proof; publicSignals: string[]; statement?: ZkStatementBinding } — ZK polygon containment proof
   * - custom: { verifier_id: string; [key: string]: unknown }
   */
  data: Record<string, unknown>;
}

/**
 * Verification result for a single proof
 */
export interface ProofResult {
  method: ProofMethodType;
  verified: boolean;
  /** Verification details (method-specific) */
  details: Record<string, unknown>;
}

/**
 * Combined result of all verifications
 */
export interface VerificationResult {
  verified: boolean;
  /** Result for each proof method */
  proofs: ProofResult[];
  /** GPS-based location proof (when GPS verification is included) */
  location_proof?: LocationProof;
  timestamp: string;
}

/**
 * Custom verifier function type
 * Interface for SDK consumers to register custom verification logic
 */
export type ProofVerifier = (
  requirement: ProofRequirement,
  submission: ProofSubmission,
  drop: GeoDrop
) => Promise<ProofResult> | ProofResult;

